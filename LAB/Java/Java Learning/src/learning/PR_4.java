package learning;

public class PR_4 {
    public static void main(String[] args) {
        System.out.println("Literals:-");
        //There are different types of literal
        //Data of various data type are called literal of that respective datatype.Eg-
        int age=19;     // 19 - integer literal
        char ch = 'A';  // A - character literal. Remember to keep characters in single quote
        String st = "This is good"; // This is good - string literal. Remember to keep string in double quote

        System.out.println(age);
        System.out.println(ch);
        System.out.println(st);
    }
}
